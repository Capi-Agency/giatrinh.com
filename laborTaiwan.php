<p><span style="font-size: 120%;">&nbsp; &nbsp; Trường Cao đẳng Kinh tế Công nghệ Hà Nội Trực thuộc Bộ Lao động Thương binh và Xã hội Thông báo tuyển sinh Chương Trình đi&nbsp;Xuất Khẩu Lao Động Đài Loan. Chi Phí đi&nbsp;Xuất Khẩu Lao Động Đài Loan chuẩn theo quy định từ Bộ LĐTBXH (Cam kết không phát sinh). Cam kết hỗ trợ việc làm với thu nhập ổn định. Hỗ trợ làm đầy đủ giấy tờ thủ tục.</span></p>
<p style="text-align: center;"><span style="font-size: 160%; color: #ed1c24;"><strong>THÔNG BÁO TUYỂN DỤNG XUẤT KHẨU LAO ĐỘNG ĐI ĐÀI LOAN</strong></span></p>
<p style="text-align: center;"><span style="font-size: 130%; color: #ed1c24;"><strong>Theo Chương trình Hỗ trợ đi Xuất Khẩu Lao Động&nbsp;Từ Bộ Lao Động Thương Binh Xã Hội</strong></span></p>
<p><span style="font-size: 120%;">&nbsp; &nbsp; Trường Cao đẳng Kinh tế Công nghệ Hà Nội được thành lập theo Quyết định số 594/QĐ-LĐTBXH ngày 12 tháng 5 năm 2010 của Bộ Lao động Thương binh và Xã hội. Thực hiện chủ trương của Nhà nước về việc nâng cao nguồn nhân lực trong nước, Trường Cao đẳng Kinh tế Công nghệ Hà Nội thông báo tuyển&nbsp;dụng&nbsp;Xuất Khẩu Lao Động đi Đài Loan. Để biết chi tiết về các đơn hàng của chương trình hãy gọi đến&nbsp;<span style="color: #ed1c24;"><strong>Hotline: 0862.416.198</strong></span>&nbsp;Dưới đây là một số thông tin cơ bản.</span></p>
<p>&nbsp;</p>
<p><span style="font-size: 120%; color: #ed1c24;"><strong>CHƯƠNG TRÌNH XUẤT KHẨU LAO ĐỘNG ĐI ĐÀI LOAN</strong></span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Đối tượng và ngành nghề tuyển dụng cơ bản:</strong></span></p>
<p><span style="font-size: 120%;">– Nam, nữ: Tốt nghiệp PTTH hoặc các Trường Trung cấp, Cao đẳng, Đại học.</span></p>
<p><span style="font-size: 120%;">– Độ tuổi: từ 18&nbsp;– 40</span></p>
<p><span style="font-size: 120%;">– Sức khỏe tốt, không mắc các bệnh truyền nhiễm: Viêm gan B, C, HIV…</span></p>
<p><span style="font-size: 120%;">– Ý thức làm việc chăm chỉ, tính kỷ luật cao, có kinh nghiệm làm việc, có bảo hiểm xã hội 2 năm trở lên là một lợi thế.</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Ngành nghề và số lượng:</strong></span></p>
<p><span style="font-size: 120%;">– Ngành nghề:&nbsp;Xây Dựng, Công xưởng.</span></p>
<p><span style="font-size: 120%;">– Số lượng: Lớn hơn 10.</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Quyền lợi&nbsp;và Các điều kiện khác</strong></span></p>
<p><span style="font-size: 120%;">– Được đi Xuất Khẩu Lao Động theo Chương trình và Chi Phí quy định từ Bộ Lao Động Thương Binh Xã Hội.</span></p>
<p><span style="font-size: 120%;">– Hỗ trợ đầy đủ giấy tờ thủ tục khi đi Xuất Khẩu Lao Động. Hỗ trợ vay ngân hàng khi&nbsp;Người lao động&nbsp;yêu cầu.</span></p>
<p><span style="font-size: 120%;">– Nhà trường cùng Bộ LĐTBXH Cam kết hỗ trợ việc làm với thu nhập ổn định cho&nbsp;người lao động đi Xuất khẩu.&nbsp;Vừa có tiền ăn, ở, chi tiêu và có thêm tiền tích lũy.</span></p>
<p><span style="font-size: 120%;">–&nbsp;Lương làm thêm nhiều, các chế độ đãi ngộ tốt và các quyền lợi theo đúng pháp luật của nước bạn đi Xuất Khẩu Lao Động.</span></p>
<p><span style="font-size: 120%;">– Điều kiện xuất cảnh: Cần có chứng chỉ tiếng của nước bạn đi&nbsp;Xuất Khẩu Lao Động.</span></p>
<p><span style="font-size: 120%;">– Thời gian xuất cảnh: 3 – 4 tháng sau khi trúng tuyển.</span></p>
<p><span style="font-size: 120%;">– Hợp đồng: Tối thiểu 3 năm, sau đó vẫn được tiếp tục gia hạn hợp đồng.</span></p>
<p><span style="font-size: 120%; color: #0000ff;"><em><strong>– Liên tục nhận hồ hơ và thi tuyển nhiều đợt trong năm.</strong></em></span></p>
<p>&nbsp;</p>
<p><span style="font-size: 120%; color: #008000;"><strong><span style="color: #ed1c24;">QUY TRÌNH ĐI XUẤT KHẨU LAO ĐỘNG CƠ BẢN</span>&nbsp;<span style="color: #282828;"><em>(Đây là&nbsp;quy trình&nbsp;cơ bản, các nước khác nhau thời gian học có thể khác nhau. Hãy gọi vào Hotline để được tư vẫn nhanh và hỗ&nbsp;trợ miễn phí 24/7)</em></span></strong></span></p>
<p><span style="font-size: 120%;">– Học viên quan tâm đến Xuất Khẩu Lao Động gọi điện đến Hotline sẽ được tư vấn hướng dẫn chuẩn bị hồ sơ giấy tờ cần thiết theo mẫu của nhà trường.</span></p>
<p><span style="font-size: 120%;">– Tiếp theo bạn (Có thể đi cùng phụ huynh) mang hồ sơ đến trường để được tư vấn trực tiếp. Dựa vào trình độ học vấn, bằng cấp, kinh nghiệm làm việc, khả năng và tình hình tài chính của bạn, cán bộ tư vấn sẽ giúp bạn lựa chọn quyết định phù hợp nhất.</span></p>
<p><span style="font-size: 120%;">– Khi đã chọn được&nbsp;chương chình và nước&nbsp;phù hợp để đi Xuất Khẩu Lao Động. Bạn cần nộp tiền đặt cọc và Nhà trường sẽ chuẩn bị cho bạn toàn bộ giấy tờ thủ tục liên quan.</span></p>
<p><span style="font-size: 120%;">– Trong thời gian chờ bay bạn cần phải hoàn trình khóa học Tiếng và tác phong từ 4 – 9 tháng tại trường (Xuất Khẩu Lao Động Nghề cần thêm chứng chỉ nghề). Trong quá trình học bạn sẽ được hướng dẫn toàn bộ về những vấn đề cần chú ý và kinh nghiệm khi đi&nbsp;Xuất Khẩu Lao Động&nbsp;ở nước Ngoài.</span></p>
<p><span style="font-size: 120%;">– Sau khi hoàn thành khóa học và các thủ tục liên quan bạn sẽ được Nhà trường chuẩn bị toàn bộ thủ tục bao gồm: đặt vé máy bay, đăng ký chuyến bay phù hợp, đưa đón&nbsp;người lao động&nbsp;tại sân bay, sắp xếp chỗ ăn ở cho&nbsp;người lao động&nbsp;theo yêu cầu… Và tất cả các vấn đề liên quan khác.</span></p>
<p><span style="font-size: 120%;">– Khi đã qua đến nước bạn Xuất Khẩu Lao Động. Bạn sẽ theo chương trình&nbsp;làm việc&nbsp;tại nơi bạn quy định. Bạn sẽ được cung cấp số điện thoại Hotline, địa chỉ văn phòng Hỗ trợ tại nước sở tại. Bạn có thể đăng ký việc làm thêm tại đây và có thể ứng tiền tại Văn phòng khi chưa có tiền tiêu dùng.</span></p>
