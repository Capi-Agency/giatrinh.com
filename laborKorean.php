<p><span style="font-size: 120%;">– Khi đã chọn được&nbsp;chương chình và nước&nbsp;phù hợp để đi Xuất Khẩu Lao Động. Bạn cần nộp tiền đặt cọc và Nhà trường sẽ chuẩn bị cho bạn toàn bộ giấy tờ thủ tục liên quan.</span></p>
<p><span style="font-size: 120%;">– Trong thời gian chờ bay bạn cần phải hoàn trình khóa học Tiếng và tác phong từ 4 – 9 tháng tại trường (Xuất Khẩu Lao Động Nghề cần thêm chứng chỉ nghề). Trong quá trình học bạn sẽ được hướng dẫn toàn bộ về những vấn đề cần chú ý và kinh nghiệm khi đi&nbsp;Xuất Khẩu Lao Động&nbsp;ở nước Ngoài.</span></p>
<p><span style="font-size: 120%;">– Sau khi hoàn thành khóa học và các thủ tục liên quan bạn sẽ được Nhà trường chuẩn bị toàn bộ thủ tục bao gồm: đặt vé máy bay, đăng ký chuyến bay phù hợp, đưa đón&nbsp;người lao động&nbsp;tại sân bay, sắp xếp chỗ ăn ở cho&nbsp;người lao động&nbsp;theo yêu cầu… Và tất cả các vấn đề liên quan khác.</span></p>
