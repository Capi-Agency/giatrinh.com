<p><span style="font-size: 120%;">–&nbsp;Ưu tiên hồ sơ các học viên sinh năm 1997 đến năm 2000 có điểm học bạ 03 năm THPT tối thiểu điểm từ 6.0 trở lên. Đối với các học viên sinh năm 1991 đến năm 1996 phải có bằng tốt nghiệp Trung cấp, hoặc Cao đẳng, Đại học có điểm từ 6.5 trở lên.</span></p>
<p><span style="font-size: 120%;">– Không bị mắc lao phổi, không có tiền án tiền sự, không có người thân trong gia đình (cùng sổ hộ khẩu) đang cư trú bất hợp pháp ở Hàn Quốc</span><span style="font-size: 120%;">.</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Quyền lợi của học viên khi đi Du Học&nbsp;Hàn Quốc</strong></span></p>
<p><span style="font-size: 120%;">– Bay ngay sau từ 3 – 4 tháng nộp hồ sơ.</span></p>
<p><span style="font-size: 120%;">– Được đi Du Học theo Chương trình và Chi Phí chuẩn theo quy định.</span></p>
<p><span style="font-size: 120%;">– Hỗ trợ đầy đủ giấy tờ thủ tục khi đi Du học. Hỗ trợ vay ngân hàng khi Sinh viên yêu cầu.</span></p>
<p><span style="font-size: 120%;">–&nbsp;Được chọn vào Trường hàng đầu của Hàn Quốc, đảm bảo đỗ Visa.</span></p>
<p><span style="font-size: 120%;">– Được bố trí việc làm trong quá trình học tập (đủ chi phí ăn học).</span></p>
<p><span style="font-size: 120%;">– Được&nbsp;hỗ trợ đầy đủ tại Hàn Quốc&nbsp;khi sang nhập học.</span></p>
<p><span style="font-size: 120%;">– Được cung cấp số điện thoại Hotline, địa chỉ văn phòng tư vấn và có thể ứng tiền tại Văn phòng khi chưa có tiền ăn học.</span></p>
<p><span style="font-size: 120%;">– Hàng năm được tặng Vé máy bay khứ hồi về ăn Tết nếu có thành tích học tập tốt (tặng 15-20% tổng số học viên của Trường).</span></p>
<p><span style="font-size: 120%;">– Được 500 Doanh nghiệp lớn của Hàn Quốc nhận vào làm việc sau khi ra Trường để cấp Visa E7 cư trú dài hạn tại Hàn Quốc.</span></p>
