<p><span style="font-size: 120%; color: #008000;"><strong>Kinh phí Du học</strong></span></p>
<p><span style="font-size: 120%;">– Các chi phí chương trình du học tiếng visa D4-1 bạn cần phải đóng bao gồm: 01 năm học phí + 06 tháng ký túc xá + 01 năm bảo hiểm + phí nhập học + phí làm hồ sơ tại Việt Nam + phí hỗ trợ việc làm + Vé máy bay 1 chiều, chứng minh tài chính…</span></p>
