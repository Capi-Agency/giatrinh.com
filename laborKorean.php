<p style="text-align: center;"><span style="font-size: 160%; color: #ed1c24;"><strong>THÔNG BÁO TUYỂN DỤNG XUẤT KHẨU LAO ĐỘNG ĐI&nbsp;HÀN QUỐC</strong></span></p>
<p style="text-align: center;"><span style="font-size: 130%; color: #ed1c24;"><strong>Theo Chương trình Hỗ trợ đi Xuất Khẩu Lao Động và Du học Cấp Phép Từ Bộ LĐTHXH</strong></span></p>
<p><span style="font-size: 120%;">&nbsp; &nbsp; Trường Cao đẳng Kinh tế Công nghệ Hà Nội được thành lập theo Quyết định số 594/QĐ-LĐTBXH ngày 12 tháng 5 năm 2010 của Bộ Lao động Thương binh và Xã hội. Thực hiện chủ trương của Nhà nước về việc nâng cao nguồn nhân lực trong nước, Trường Cao đẳng Kinh tế Công nghệ Hà Nội thông báo tuyển&nbsp;dụng&nbsp;Xuất Khẩu Lao Động và Du Học đi Hàn Quốc. Để biết chi tiết về các đơn hàng của chương trình hãy gọi đến&nbsp;<span style="color: #ed1c24;"><strong>Hotline: 0904.353.062</strong></span>. Tùy điều kiện phù hợp bạn có thể lựa chọn một trong hai chương trình sau:</span></p>
<p><span style="font-size: 120%; color: #0000ff;"><strong>– TUYỂN KỸ SƯ ĐI HÀN (VISA E7-1)</strong></span></p>
<p><span style="font-size: 120%; color: #0000ff;"><strong>–&nbsp;DU HỌC NGHỀ TẠI HÀN QUỐC (VISA D4-6)</strong></span></p>
<p><span style="font-size: 120%; color: #0000ff;"><strong>– DU HỌC&nbsp;TIẾNG TẠI HÀN QUỐC&nbsp;(VISA D4-1)</strong></span></p>
<p><span style="font-size: 120%; color: #ed1c24;"><strong>1. CHƯƠNG TRÌNH&nbsp;TUYỂN KỸ SƯ&nbsp;ĐI HÀN QUỐC</strong></span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Đối tượng và ngành nghề tuyển dụng cơ bản:</strong></span></p>
<p><span style="font-size: 120%;">– Nam: Tốt nghiệp Cao đẳng, Đại học chuyên ngành Điện, Điện tử, Cơ khí, Công nghệ, Xây dựng…</span></p>
<p><span style="font-size: 120%;">– Độ tuổi:&nbsp;Từ&nbsp;22 –&nbsp;38</span></p>
<p><span style="font-size: 120%;">– Sức khỏe tốt, không mắc các bệnh truyền nhiễm: Viêm gan B, C, HIV…</span></p>
<p><span style="font-size: 120%;">– Ý thức làm việc chăm chỉ, tính kỷ luật cao, có kinh nghiệm làm việc, có bảo hiểm xã hội 2 năm trở lên là một lợi thế.</span></p>
<p><span style="font-size: 120%;"><em><span style="color: #0000ff;">–<strong>&nbsp;Ngành nghề:</strong></span></em></span><span style="font-size: 120%;">&nbsp;Điện, Điện tử, Cơ khí, Công nghệ, Xây dựng… (Học tiếng định hướng 1 tháng)</span></p>
<p><span style="font-size: 120%;"><span style="color: #0000ff;"><em><strong>– Số lượng:</strong></em></span>&nbsp;Lớn hơn 50</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Quyền lợi&nbsp;và Các điều kiện khác</strong></span></p>
<p><span style="font-size: 120%;">– Được đi Xuất Khẩu Lao Động theo Chương trình và Chi Phí chuẩn theo quy định.</span></p>
<p><span style="font-size: 120%;">– Hỗ trợ đầy đủ giấy tờ thủ tục khi đi Xuất Khẩu Lao Động. Hỗ trợ thủ tục vay ngân hàng khi&nbsp;Người lao động&nbsp;yêu cầu.</span></p>
<p><span style="font-size: 120%;">– Nhà trường cam kết lương cơ bản tối thiểu từ 40 triệu /tháng trở lên, việc làm thêm nhiều.</span></p>
<p><span style="font-size: 120%;">–&nbsp;Lương làm thêm nhiều, các chế độ đãi ngộ tốt và các quyền lợi theo đúng pháp luật của nước bạn đi Xuất Khẩu Lao Động.</span></p>
<p><span style="font-size: 120%;">– Điều kiện xuất cảnh: Hồ sơ đạt yêu cầu theo từng ngành tuyển dụng quy định.</span></p>
<p><span style="font-size: 120%;">– Thời gian xuất cảnh:&nbsp;Sau khoảng&nbsp;4 – 6 Tháng.</span></p>
<p><span style="font-size: 120%;">– Hợp đồng:&nbsp;Từ&nbsp;3 – 5&nbsp;năm tùy từng chương trình, sau đó vẫn được tiếp tục gia hạn hợp đồng.</span></p>
<p><span style="font-size: 120%; color: #0000ff;"><em><strong>– Liên tục nhận hồ hơ và thi tuyển nhiều đợt trong năm.</strong></em></span></p>
<p><span style="font-size: 120%; color: #ed1c24;"><strong>2. CHƯƠNG TRÌNH DU HỌC&nbsp;NGHỀ&nbsp;TẠI HÀN QUỐC</strong></span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Đối tượng tuyển sinh và Ngành học:</strong></span></p>
<p><span style="font-size: 120%;">– Độ tuổi:&nbsp;Nam, nữ tuổi từ 18 – 30 tuổi (Ngoài độ tuổi liên hệ trực tiếp để xét hồ sơ).</span></p>
<p><span style="font-size: 120%;">– Trình độ: Tốt nghiệp THPT, Trung cấp, Cao đẳng, Đại Học.</span></p>
<p><span style="font-size: 120%;">– Ngành học: Điện, Điện dân dụng, Thời trang, Làm đẹp (Spa), Nấu ăn, Du lịch.</span></p>
<p><span style="font-size: 120%;">– Các bạn có kinh nghiệm làm việc đúng chuyên ngành sẽ được ưu tiên, chưa có kinh nghiệm sẽ được đào tạo.</span></p>
<p><span style="font-size: 120%;">– Không bị mắc lao phổi, không có tiền án tiền sự, không có người thân trong gia đình (cùng sổ hộ khẩu) đang cư trú bất hợp pháp ở Hàn Quốc.</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Quyền lợi sau khi tham gia chương trình:</strong></span></p>
<p><span style="font-size: 120%;">– Học và thi tiếng từ 4 – 5 tháng. Bay ngay sau khi có chứng chỉ tiếng</span></p>
<p><span style="font-size: 120%;">– Học 2 buổi/ 1 tuần, thời gian còn lại đi làm với đầy đủ quyền lợi như lao động tại Hàn.</span></p>
<p><span style="font-size: 120%;">– Được đi Du Học theo Chương trình và Chi Phí chuẩn theo quy định.</span></p>
<p><span style="font-size: 120%;">– Hỗ trợ đầy đủ giấy tờ thủ tục khi đi Du học. Hỗ trợ vay ngân hàng khi Sinh viên yêu cầu.</span></p>
<p><span style="font-size: 120%;">– Được chọn vào Trường hàng đầu của Hàn Quốc, đảm bảo đỗ Visa.</span></p>
<p><span style="font-size: 120%;">– Được đào tạo tiếng đảm bảo thi đỗ chứng chỉ&nbsp;Topik 1, Topik 2, Klat 2.</span></p>
<p><span style="font-size: 120%;">– Được bố trí việc làm, lương từ 30 triệu trở lên (đã trừ chi phí ăn ở).</span></p>
<p><span style="font-size: 120%;">– Được&nbsp;hỗ trợ đầy đủ tại Hàn Quốc&nbsp;khi sang nhập học, được cung cấp số điện thoại Hotline, địa chỉ văn phòng tư vấn và có thể ứng tiền tại Văn phòng khi chưa có tiền ăn học.</span></p>
<p><span style="font-size: 120%;">– Hàng năm được tặng Vé máy bay khứ hồi về ăn Tết nếu có thành tích học tập tốt (tặng 15-20% tổng số học viên của Trường).</span></p>
<p><span style="font-size: 120%;">– Được 500 Doanh nghiệp của Hàn Quốc nhận vào làm việc sau khi ra Trường.</span></p>
<p><span style="font-size: 120%;">– Được cấp chứng chỉ nghề quốc gia của Hàn Quốc.</span></p>
<p><span style="font-size: 120%;">– Sau khi tốt nghiệp được ở lại Hàn Quốc làm việc với visa D10 trong 2 năm hoặc được chuyển sang visa đi làm E7 cư trú lâu dài tại Hàn Quốc.</span></p>
<p><span style="font-size: 120%;">– Chi phí xuất cảnh thấp và cam kết không phát sinh.</span></p>
<p><span style="font-size: 120%; color: #008000;"><strong>Kinh phí Du học</strong></span></p>
<p><span style="font-size: 120%;">– Các chi phí chương trình du học tiếng visa&nbsp;D4-6 bao gồm: 06 tháng học phí + 06 tháng ký túc xá + 01 năm bảo hiểm + phí nhập học + phí làm hồ sơ tại Việt Nam + phí hỗ trợ việc làm + Vé máy bay và đón từ sân bay về trường.</span></p>
<p><span style="font-size: 120%;">– Được hỗ trợ toàn bộ thủ tục, chi phí chứng minh tài chính khi học viên có nhu cầu.</span></p>
<p><span style="font-size: 120%; color: #ed1c24;"><strong>3. CHƯƠNG TRÌNH DU HỌC&nbsp;TIẾNG TẠI HÀN QUỐC</strong></span></p>
